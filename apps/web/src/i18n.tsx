function isLanguage(value: string | null | undefined): value is Language {
  return value === 'de' || value === 'en' || value === 'tr' || value === 'ro' || value === 'es' || value === 'fr';
}

function isMaterialStatus(value: string): value is MaterialStatus {
  return value === 'available' || value === 'reserved' || value === 'picked_up';
}

function isTransportStatus(value: string): value is TransportRequestItem['status'] {
  return value === 'planned' || value === 'in_transit' || value === 'delivered';
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === 'object' && value !== null;
}

function isMaterialCreatedPayload(payload: unknown): payload is MaterialCreatedNotificationPayload {
  return isRecord(payload) && typeof payload.materialTitle === 'string' && typeof payload.actorName === 'string';
}

function isMaterialReservedPayload(payload: unknown): payload is MaterialReservedNotificationPayload {
  return isRecord(payload) && typeof payload.materialTitle === 'string' && typeof payload.actorName === 'string';
}

function isMaterialStatusChangedPayload(payload: unknown): payload is MaterialStatusChangedNotificationPayload {
  return isRecord(payload)
    && typeof payload.materialTitle === 'string'
    && typeof payload.actorName === 'string'
    && typeof payload.currentStatus === 'string'
    && isMaterialStatus(payload.currentStatus);
}

function isTransportCreatedPayload(payload: unknown): payload is TransportCreatedNotificationPayload {
  return isRecord(payload) && typeof payload.materialTitle === 'string' && typeof payload.actorName === 'string';
}

function isTransportStatusChangedPayload(payload: unknown): payload is TransportStatusChangedNotificationPayload {
  return isRecord(payload)
    && typeof payload.transportId === 'string'
    && typeof payload.currentStatus === 'string'
    && isTransportStatus(payload.currentStatus);
}

function formatTemplate(template: string, values?: Record<string, string | number>) {
  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
}

export function getInitialLanguage(): Language {
  if (typeof window === 'undefined') {
    return 'de';
  }

  const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (isLanguage(storedLanguage)) {
    return storedLanguage;
  }

  window.localStorage.removeItem(LEGACY_LANGUAGE_STORAGE_KEY);
  return 'de';
}

export interface I18nContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey, values?: Record<string, string | number>) => string;
  formatErrorMessage: (error: unknown, fallbackKey: TranslationKey) => string;
  formatNumber: (value: number, options?: Intl.NumberFormatOptions) => string;
  formatDateTime: (value: Date | string | number) => string;
  formatMaterialStatus: (status: MaterialStatus) => string;
  formatMaterialCondition: (condition: MaterialCondition) => string;
  formatTransportStatus: (status: TransportRequestItem['status']) => string;
  formatUserRole: (role: UserRole) => string;
  formatCategory: (category: string) => string;
  formatNotificationType: (type: string) => string;
  formatNotificationMessage: (notification: NotificationEventItem) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({
  children,
  language,
  setLanguage,
}: {
  children: ReactNode;
  language: Language;
  setLanguage: (language: Language) => void;
}) {
  const value = useMemo<I18nContextValue>(() => {
    const locale = localeByLanguage[language];
    const dictionary = messages[language];

    const formatNumber = (input: number, options?: Intl.NumberFormatOptions) => new Intl.NumberFormat(locale, options).format(input);
    const translate = (key: TranslationKey, values?: Record<string, string | number>) => formatTemplate(dictionary[key] ?? englishMessages[key], values);
    const formatMaterialStatusLabel = (status: MaterialStatus) => materialStatusLabels[language][status];
    const formatTransportStatusLabel = (status: TransportRequestItem['status']) => transportStatusLabels[language][status];
    const knownErrors = localizedKnownErrors[language];

    const formatErrorMessage = (error: unknown, fallbackKey: TranslationKey) => {
      if (error instanceof ApiError) {
        if (error.code && knownErrors[error.code]) {
          return knownErrors[error.code];
        }

        if (knownErrors[error.message]) {
          return knownErrors[error.message];
        }

        if (error.status === 401 && knownErrors['Invalid credentials']) {
          return knownErrors['Invalid credentials'];
        }
      }

      if (error instanceof Error) {
        if (knownErrors[error.message]) {
          return knownErrors[error.message];
        }

        if (error.message === 'Failed to fetch') {
          return knownErrors[API_UNREACHABLE_ERROR_CODE] ?? translate(fallbackKey);
        }
      }

      return translate(fallbackKey);
    };

    const formatNotificationType = (type: string) => {
      switch (type) {
        case 'material.created':
          return translate('dashboard.notificationTypeMaterialCreated');
        case 'material.reserved':
          return translate('dashboard.notificationTypeMaterialReserved');
        case 'material.status.changed':
          return translate('dashboard.notificationTypeMaterialStatus');
        case 'transport.created':
          return translate('dashboard.notificationTypeTransportCreated');
        case 'transport.status.changed':
          return translate('dashboard.notificationTypeTransportStatus');
        default:
          return type.replaceAll('.', ' · ').replaceAll('_', ' ');
      }
    };

    const formatNotificationMessage = (notification: NotificationEventItem) => {
      const payload = notification.payload;

      switch (notification.type) {
        case 'material.created':
          if (!isMaterialCreatedPayload(payload)) {
            return notification.message;
          }
          return translate('dashboard.notificationMaterialCreated', { title: payload.materialTitle, actor: payload.actorName });
        case 'material.reserved':
          if (!isMaterialReservedPayload(payload)) {
            return notification.message;
          }
          return translate('dashboard.notificationMaterialReserved', { title: payload.materialTitle, actor: payload.actorName });
        case 'material.status.changed':
          if (!isMaterialStatusChangedPayload(payload)) {
            return notification.message;
          }
          return translate('dashboard.notificationMaterialStatusChanged', {
            title: payload.materialTitle,
            status: formatMaterialStatusLabel(payload.currentStatus),
          });
        case 'transport.created':
          if (!isTransportCreatedPayload(payload)) {
            return notification.message;
          }
          return translate('dashboard.notificationTransportCreated', { title: payload.materialTitle, actor: payload.actorName });
        case 'transport.status.changed':
          if (!isTransportStatusChangedPayload(payload)) {
            return notification.message;
          }
          return translate('dashboard.notificationTransportStatusChanged', {
            id: payload.transportId,
            status: formatTransportStatusLabel(payload.currentStatus),
          });
        default:
          return notification.message;
      }
    };

    return {
      language,
      setLanguage,
      t: translate,
      formatErrorMessage,
      formatNumber,
      formatDateTime: (input) =>
        new Intl.DateTimeFormat(locale, {
          dateStyle: 'short',
          timeStyle: 'short',
        }).format(new Date(input)),
      formatMaterialStatus: formatMaterialStatusLabel,
      formatMaterialCondition: (condition) => materialConditionLabels[language][condition],
      formatTransportStatus: formatTransportStatusLabel,
      formatUserRole: (role) => userRoleLabels[language][role],
      formatCategory: (category) => categoryLabels[language][category] ?? category,
      formatNotificationType,
      formatNotificationMessage,
    };
  }, [language, setLanguage]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }

  return context;
}